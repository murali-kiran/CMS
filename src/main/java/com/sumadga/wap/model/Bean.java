package com.sumadga.wap.model;

public class Bean<K,V> {
	
	private K id;
	private V name;
	
	public K getId() {
		return id;
	}
	public void setId(K id) {
		this.id = id;
	}
	public V getName() {
		return name;
	}
	public void setName(V name) {
		this.name = name;
	}
	
	
	

}
