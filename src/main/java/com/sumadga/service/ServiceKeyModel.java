package com.sumadga.service;

public class ServiceKeyModel {

	/**
	 * @param args
	 */
	private int serviceKeyId;
	private String serviceKeyTitle;
	private String serviceKeyName;
	private String serviceKeyDescription;
	public int getServiceKeyId() {
		return serviceKeyId;
	}
	public void setServiceKeyId(int serviceKeyId) {
		this.serviceKeyId = serviceKeyId;
	}
	public String getServiceKeyTitle() {
		return serviceKeyTitle;
	}
	public void setServiceKeyTitle(String serviceKeyTitle) {
		this.serviceKeyTitle = serviceKeyTitle;
	}
	public String getServiceKeyName() {
		return serviceKeyName;
	}
	public void setServiceKeyName(String serviceKeyName) {
		this.serviceKeyName = serviceKeyName;
	}
	public String getServiceKeyDescription() {
		return serviceKeyDescription;
	}
	public void setServiceKeyDescription(String serviceKeyDescription) {
		this.serviceKeyDescription = serviceKeyDescription;
	}

	
}
